import PropTypes from "prop-types";
import React from "react";
import { useReducer } from "react";
import { Component1_79 } from "../../icons/Component1_79";
import "./style.css";

export const Component = ({
  variant,
  hover,
  className,
  icon = <Component1_79 className="component-1-79" />,
}) => {
  const [state, dispatch] = useReducer(reducer, {
    variant: variant || "one",

    hover: hover || false,
  });

  return (
    <div
      className={`component hover-${state.hover} ${state.variant} ${className}`}
      onMouseLeave={() => {
        dispatch("mouse_leave");
      }}
      onMouseEnter={() => {
        dispatch("mouse_enter");
      }}
    >
      {icon}
    </div>
  );
};

function reducer(state, action) {
  switch (action) {
    case "mouse_enter":
      return {
        ...state,
        hover: true,
      };

    case "mouse_leave":
      return {
        ...state,
        hover: false,
      };
  }

  return state;
}

Component.propTypes = {
  variant: PropTypes.oneOf(["two", "three", "four", "one", "five", "six"]),
  hover: PropTypes.bool,
};
